using System;

class Program
{
    static void Main()
    {
        // This function multiplies two numbers
        int product = Multiply(4, 5);
        Console.WriteLine("Product is: " + product);
    }

    // This function multiplies two numbers
    static int Multiply(int a, int b)
    {
        // Multiply a and b
        return a * b;
    }
}
